package dto;
import lombok.*;

@AllArgsConstructor
public abstract class BankCard {
    String number;
    User user;

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }
}
